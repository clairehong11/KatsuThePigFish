const numPages = Math.ceil(testData.length / 4);

class PhotoAlbum extends Component {

  state = {
    leftPage: 1,
    rightPage: 2
  }

  isVisible = (pageNumber) => {
    return (pageNumber === this.state.leftPage || pageNumber === this.state.rightPage)
  };

  // page to flip to
  flipPage = (flipTo) => {
    if (flipTo === 'PREV') {
      this.setState(prevState => ({
        leftPage: prevState.leftPage - 2,
        rightPage: prevState.rightPage - 2
      }));
    }
    if (flipTo === 'NEXT') {
      this.setState(prevState => ({
        leftPage: prevState.leftPage + 2,
        rightPage: prevState.rightPage + 2
      }));
    }
  };

  render() {
    const {
      leftPage,
      rightPage
    } = this.state;

    return (
      <div className="PhotoAlbumContainer">
        <div className="PhotoAlbum">
          {[...Array(numPages)].map((e, index) => 
            <Page
              key={index}
              pageNumber={index+1}
              isVisible={this.isVisible(index+1)} 
              images={testData.slice(index*4, index*4+4)}
            />
          )}
        </div>
        <div className="pagination">
          <div className="prev-page">
            {leftPage > 1 && <div className="prev-btn" onClick={() => this.flipPage('PREV')}>Prev Page</div>}
          </div>
          <div className="next-page">
            {rightPage < numPages && <div className="next-btn" onClick={() => this.flipPage('NEXT')}>Next Page</div>}
          </div>
        </div>
      </div>
    )
  }
}

export default PhotoAlbum;